@Configuration
@OpenAPIDefinition(
        info = @Info(
                title = "Card Cost API",
                version = "1.0",
                description = "REST API that calculates the clearing cost of a payment card based on the issuing country.",
                contact = @Contact(
                        name = "Cristian Cabrera",
                        email = "cmcabrera.work@gmail.com"
                ),
                license = @License(
                        name = "Apache 2.0",
                        url = "http://www.apache.org/licenses/LICENSE-2.0.html"
                )
        ),
        servers = {
                @Server(url = "http://localhost:8080", description = "Local Development Server")
        }
)
@SecurityScheme(
        name = "bearerAuth",
        type = SecuritySchemeType.HTTP,
        scheme = "bearer",
        bearerFormat = "JWT",
        description = "JWT Authentication. Requires a token obtained from /api/v1/authenticate"
)
public class OpenApiConfig {
}
